var quote = document.getElementById("quote");
var btn = document.getElementById("btn");
var option = document.querySelector("select");

const motivationQuotes = [
  "The only way to do great work is to love what you do. - Steve Jobs",
  "Believe you can and you're halfway there. - Theodore Roosevelt",
  "Dream it. Wish it. Do it. - Unknown"
];

const successQuotes = [
  "Success is not final, failure is not fatal: it is the courage to continue that counts. - Winston Churchill",
  "Opportunities don't happen. You create them. - Chris Grosser",
  "The best revenge is massive success. - Frank Sinatra"
];

const happinessQuotes = [
  "Happiness depends upon ourselves. - Aristotle",
  "Keep your face always toward the sunshine—and shadows will fall behind you. - Walt Whitman",
  "Difficult roads often lead to beautiful destinations. - Unknown"
];

function generateQuote() {
  let selectedCategory = option.value;

  if (selectedCategory === "Happy") {
    showQuote(happinessQuotes);
    document.body.classList.add("img")
  } else if (selectedCategory === "Success") {
    showQuote(successQuotes);
  } else if (selectedCategory === "Motivational") {
    showQuote(motivationQuotes);
  } else {
    quote.textContent = "Please select a valid category!";
  }
}

function showQuote(quotesArray) {
  let randomIndex = Math.floor(Math.random() * quotesArray.length);
  quote.textContent = quotesArray[randomIndex];
}

btn.addEventListener("click", generateQuote);



